function makeStyleLoader( type ) { // #38
    const cssLoader = {
        loader: 'css-loader',
        options: {
            minimize: env.production // #39
        }
    };
    const loaders = [ cssLoader ];
    if ( type ) // #40
        loaders.push( type + '-loader' );
    if ( env.production ) {
        return ExtractTextPlugin.extract( { // #41
            use: loaders,
            fallback: 'vue-style-loader'
        } );
    } else { // #42
        return [ 'vue-style-loader' ].concat( loaders );
    }
}



